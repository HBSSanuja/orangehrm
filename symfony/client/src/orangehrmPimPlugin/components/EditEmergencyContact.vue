<template>
  <div class="orangehrm-horizontal-padding orangehrm-vertical-padding">
    <oxd-text tag="h6">Edit Emergency Contact</oxd-text>
    <oxd-divider />
    <oxd-form :loading="isLoading" @submitValid="onSave">
      <oxd-form-row>
        <oxd-grid :cols="3" class="orangehrm-full-width-grid">
          <oxd-grid-item>
            <oxd-input-field
              label="Name"
              v-model="contact.name"
              :rules="rules.name"
              required
            />
          </oxd-grid-item>
          <oxd-grid-item>
            <oxd-input-field
              label="Relationship"
              v-model="contact.relationship"
              :rules="rules.relationship"
              required
            />
          </oxd-grid-item>
        </oxd-grid>
      </oxd-form-row>
      <oxd-form-row>
        <oxd-grid :cols="3" class="orangehrm-full-width-grid">
          <oxd-grid-item>
            <oxd-input-field
              label="Home Telephone"
              v-model="contact.homePhone"
              :rules="rules.homePhone"
            />
          </oxd-grid-item>
          <oxd-grid-item>
            <oxd-input-field
              label="Mobile"
              v-model="contact.mobilePhone"
              :rules="rules.mobilePhone"
            />
          </oxd-grid-item>
          <oxd-grid-item>
            <oxd-input-field
              label="Work Telephone"
              v-model="contact.officePhone"
              :rules="rules.officePhone"
            />
          </oxd-grid-item>
        </oxd-grid>
      </oxd-form-row>

      <oxd-form-actions>
        <oxd-button
          type="button"
          displayType="ghost"
          label="Cancel"
          @click="onCancel"
        />
        <submit-button />
      </oxd-form-actions>
    </oxd-form>
  </div>
  <oxd-divider />
</template>

<script>
const emergencyContactModel = {
  name: '',
  relationship: '',
  homePhone: '',
  officePhone: '',
  mobilePhone: '',
};

export default {
  name: 'edit-emergency-contact',

  emits: ['close'],

  props: {
    http: {
      type: Object,
      required: true,
    },
    data: {
      type: Object,
      required: true,
    },
  },

  data() {
    return {
      isLoading: false,
      contact: {...emergencyContactModel},
      rules: {
        name: [
          v => {
            return (!!v && v.trim() !== '') || 'Required';
          },
          v => {
            return !v || v?.length <= 100 || 'Should not exceed 100 characters';
          },
        ],
        relationship: [
          v => {
            return (!!v && v.trim() !== '') || 'Required';
          },
          v => {
            return !v || v?.length <= 100 || 'Should not exceed 100 characters';
          },
        ],
        homePhone: [
          v => {
            return (
              v.trim() !== '' ||
              this.contact.mobilePhone.trim() !== '' ||
              this.contact.officePhone.trim() !== '' ||
              'At least one phone number is required'
            );
          },
          v => {
            return !v || v?.length <= 30 || 'Should not exceed 30 characters';
          },
          v => {
            return !v || v.match(/[0-9+()-]+$/)
              ? true
              : false || 'Allows numbers and only + - / ( )';
          },
        ],
        mobilePhone: [
          v => {
            return !v || v?.length <= 30 || 'Should not exceed 30 characters';
          },
          v => {
            return !v || v.match(/[0-9+()-]+$/)
              ? true
              : false || 'Allows numbers and only + - / ( )';
          },
        ],
        officePhone: [
          v => {
            return !v || v?.length <= 30 || 'Should not exceed 30 characters';
          },
          v => {
            return !v || v.match(/[0-9+()-]+$/)
              ? true
              : false || 'Allows numbers and only + - / ( )';
          },
        ],
      },
    };
  },

  methods: {
    onSave() {
      this.isLoading = true;
      this.http
        .update(this.data.id, {
          ...this.contact,
        })
        .then(() => {
          return this.$toast.success({
            title: 'Success',
            message: 'Successfully Updated',
          });
        })
        .then(() => {
          this.contact = {...emergencyContactModel};
          this.onCancel();
        });
    },
    onCancel() {
      this.$emit('close', true);
    },
  },

  beforeMount() {
    this.isLoading = true;
    this.http
      .get(this.data.id)
      .then(response => {
        const {data} = response.data;
        this.contact = {...emergencyContactModel, ...data};
      })
      .finally(() => {
        this.isLoading = false;
      });
  },
};
</script>
